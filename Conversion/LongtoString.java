// Java program to convert Long to String
// using + operator

public class LongtoString {

	// main method
	public static void main(String args[])
	{

		// create a Long
		Long varLong = 999999999999L;

		// convert into String
		String str = varLong + " ";

		// print Long as String
		System.out.println(str);

        //valueOf(),toString()
	}
}
